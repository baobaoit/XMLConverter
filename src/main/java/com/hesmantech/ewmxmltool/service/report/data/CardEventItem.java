package com.hesmantech.ewmxmltool.service.report.data;

public class CardEventItem {
    private String cardId;
    private String cardProduct;
    private String eventType;
    private String eventDate;
    private String eventOldCardStatus;
    private String eventCardStatus;
    private String eventActivationDate;
    private String eventSource;
    private String eventReasonCode;
    private String eventNote;
    private String eventOriginatorId;

    public String getCardId() {
        return cardId;
    }

    public void setCardId(String cardId) {
        this.cardId = cardId;
    }

    public String getCardProduct() {
        return cardProduct;
    }

    public void setCardProduct(String cardProduct) {
        this.cardProduct = cardProduct;
    }

    public String getEventType() {
        return eventType;
    }

    public void setEventType(String eventType) {
        this.eventType = eventType;
    }

    public String getEventDate() {
        return eventDate;
    }

    public void setEventDate(String eventDate) {
        this.eventDate = eventDate;
    }

    public String getEventOldCardStatus() {
        return eventOldCardStatus;
    }

    public void setEventOldCardStatus(String eventOldCardStatus) {
        this.eventOldCardStatus = eventOldCardStatus;
    }

    public String getEventCardStatus() {
        return eventCardStatus;
    }

    public void setEventCardStatus(String eventCardStatus) {
        this.eventCardStatus = eventCardStatus;
    }

    public String getEventActivationDate() {
        return eventActivationDate;
    }

    public void setEventActivationDate(String eventActivationDate) {
        this.eventActivationDate = eventActivationDate;
    }

    public String getEventSource() {
        return eventSource;
    }

    public void setEventSource(String eventSource) {
        this.eventSource = eventSource;
    }

    public String getEventReasonCode() {
        return eventReasonCode;
    }

    public void setEventReasonCode(String eventReasonCode) {
        this.eventReasonCode = eventReasonCode;
    }

    public String getEventNote() {
        return eventNote;
    }

    public void setEventNote(String eventNote) {
        this.eventNote = eventNote;
    }

    public String getEventOriginatorId() {
        return eventOriginatorId;
    }

    public void setEventOriginatorId(String eventOriginatorId) {
        this.eventOriginatorId = eventOriginatorId;
    }
}
